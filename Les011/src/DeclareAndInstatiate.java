public class DeclareAndInstatiate {

    public static void main(String[] args) {
        //declaration (creating a space in memory), instantiation (setting a value)
        var number1 = 10;
        System.out.println("Number1 is" + number1);
        var number2 = 5;
        System.out.println("Number2 is" + number2);
        number2 = 10;
        System.out.println("Number2 is" + number2);
        number2 = 35;
        System.out.println("Number2 is" + number2);
        number2 = 50;
        System.out.println("Number2 is" + number2);


    }
}
